    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        newPass = jTextField3.getText();
        try {
            String url = "jdbc:ucanaccess://C:\\Users\\HP\\Documents\\NetBeansProjects\\DigitalDiary\\DiDiaryDB.accdb";
            java.sql.Connection conn = DriverManager.getConnection(url);

            // Validate inputs
            if (newPass == null || newPass.isEmpty() || username == null || username.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Password or username cannot be empty.");
                return;
            }

            // SQL query to update user credentials
            String query = "UPDATE Profile SET password = ? WHERE userName = ?";
            PreparedStatement stmt = conn.prepareStatement(query);
            stmt.setString(1, newPass);
            stmt.setString(2, username);

            // Execute the query
            int rowsAffected = stmt.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Password successfully changed.");
            } else {
                JOptionPane.showMessageDialog(this, "User not found. Password not updated.");
            }

            // Close resources
            stmt.close();
            conn.close();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

