     public void displayEntry() throws ClassNotFoundException, SQLException
    {
         try {
         String url = "jdbc:ucanaccess://C:\\Users\\HP\\Documents\\NetBeansProjects\\DigitalDiary\\DiDiaryDB.accdb";
         java.sql.Connection conn = DriverManager.getConnection(url); // Establish connection
         // SQL query to retrieve email and password based on username
         String query = "SELECT password FROM Profile WHERE userName = ?";
         PreparedStatement pstmt = conn.prepareStatement(query);
         pstmt.setString(1, username); // Pass the username variable
         ResultSet rs = pstmt.executeQuery();
         if (rs.next()) {
             // Retrieve email and password from the ResultSet
             String retrievedPassword = rs.getString("password");
             // Display the email and password in jTextField2 and jTextField3
             jTextField2.setText(retrievedPassword);
         } else {
             // No record found
             JOptionPane.showMessageDialog(this, "No user found with the given username.");
         }
         // Close resources
         rs.close();
         pstmt.close();
         conn.close();
     } catch (SQLException ex) {
         JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
     }
    }
