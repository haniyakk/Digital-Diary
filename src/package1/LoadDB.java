    public void displayEntry() throws ClassNotFoundException, SQLException
    {
        String url = "jdbc:ucanaccess://C:\\Users\\HP\\Documents\\NetBeansProjects\\DigitalDiary\\DiDiaryDB.accdb";
        Connection conn = DriverManager.getConnection(url);
        String sql = "SELECT * FROM Entry WHERE username = ?";
        PreparedStatement stmt = conn.prepareStatement(sql);
        stmt.setString(1, this.username);
        ResultSet rs = stmt.executeQuery();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); 
        if(!rs.isBeforeFirst())
        {
            JOptionPane.showMessageDialog(this,"No data found for this user");
        }
        else
        {
            while (rs.next()) {
            Object[] row = {
                rs.getString("ID"),
                rs.getString("entry"),
                rs.getString("entryDate"),
                rs.getString("entryTime")
            };
            model.addRow(row);
        }
        rs.close();
        stmt.close();
        conn.close();
    }
}
