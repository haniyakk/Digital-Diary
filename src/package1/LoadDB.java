    private void jButtonUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUPDATEActionPerformed
        int num = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if(num>=0)
        {
            model.setValueAt(jTextFieldID.getText(), num, 0);
            model.setValueAt(jTextFieldEn.getText(), num, 1);
            model.setValueAt(jTextFieldD.getText(), num, 2);
            model.setValueAt(jTextFieldT.getText(), num, 3);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Error");
        }
        String url = "jdbc:ucanaccess://C:\\Users\\HP\\Documents\\NetBeansProjects\\DigitalDiary\\DiDiaryDB.accdb";
    Connection conn = null;
    PreparedStatement stmt = null;
    int rowCount = model.getRowCount();

    try {
        conn = DriverManager.getConnection(url);

        String sql = "UPDATE Entry SET entry = ?, entryDate = ?, entryTime = ? WHERE ID = ? ";
        stmt = conn.prepareStatement(sql);
        for (int i = 0; i < rowCount; i++) {
            String id = model.getValueAt(i, 0).toString();
            String entry = model.getValueAt(i, 1).toString();
            String entryDate = model.getValueAt(i, 2).toString();
            String entryTime = model.getValueAt(i, 3).toString();
            stmt.setString(1, entry);
            stmt.setString(2, entryDate);
            stmt.setString(3, entryTime);
            stmt.setString(4, id);
            stmt.executeUpdate();
        }
        JOptionPane.showMessageDialog(this, "Entry Updated Successfully!");
    } catch (SQLException ex) {
        Logger.getLogger(LoadDB.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
    } finally {
        try {
            if (stmt != null) stmt.close();
            if (conn != null) conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(LoadDB.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    }//GEN-LAST:event_jButtonUPDATEActionPerformed

